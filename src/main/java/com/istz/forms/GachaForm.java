package com.istz.forms;

/**
 * @author Riki
 *
 */
public class GachaForm {

	private String gacha;
	private String times;
	/**
	 * @return gacha
	 */
	public String getGacha() {
		return gacha;
	}
	/**
	 * @param gacha セットする gacha
	 */
	public void setGacha(String gacha) {
		this.gacha = gacha;
	}
	/**
	 * @return times
	 */
	public String getTimes() {
		return times;
	}
	/**
	 * @param times セットする times
	 */
	public void setTimes(String times) {
		this.times = times;
	}

}
